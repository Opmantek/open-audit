                                <tr>
                                    <?= collection_button_read($meta->collection, $item->id) ?>
                                    <?= collection_button_devices(url_to('devicesCollection') . '?devices.location_id=' . $item->id . '&properties=audit_status,devices.id,devices.icon,devices.type,devices.name,devices.ip,devices.manufacturer,devices.os_family,devices.status,locations.name') . "\n" ?>
                                    <?php foreach ($meta->data_order as $key) {
                                        if ($key === 'id' or $key === 'orgs.id') {
                                            continue;
                                        }
                                        if ($key === 'orgs.name' and !empty($item->attributes->{'orgs.id'})) {
                                            echo "<td><a href=\"" . url_to($meta->collection . 'Collection') . "?" . $meta->collection . ".org_id=" . $item->attributes->{'orgs.id'} . "\">" . $item->attributes->{$key} . "</a></td>\n";
                                        } elseif (in_array($key, ['type', 'city', 'state', 'country'])) {
                                            echo "<td><a href=\"" . url_to($meta->collection . 'Collection') . "?" . $meta->collection . "." . $key . "=" . urlencode($item->attributes->{$key}) . "\">" . $item->attributes->{$key} . "</a></td>\n";
                                        } else {
                                            echo "<td>" . $item->attributes->{$key} . "</td>\n";
                                        }
                                        ?>
                                    <?php } ?>
                                    <?php if (strpos($user->permissions[$meta->collection], 'd') !== false) {
                                        if ($item->id === 1) {
                                            echo "<td></td>\n";
                                        } else {
                                            echo collection_button_delete(intval($item->id)) . "\n";
                                        }
                                    } ?>
                                </tr>
                                <?php } ?>
